// Writes src/mode.generated.ts with the BRIDGE_MODE constant for the current
// build. Called from `npm run build:kiosk` / `build:admin` / `dev:kiosk` /
// `dev:admin` (npm scripts set BRIDGE_MODE in the environment).

import { writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const __dirname = dirname(fileURLToPath(import.meta.url))
const target = join(__dirname, "..", "src", "mode.generated.ts")

const mode = process.env.BRIDGE_MODE
if (mode !== "kiosk" && mode !== "admin") {
  console.error(
    `inject-mode: BRIDGE_MODE env var must be "kiosk" or "admin" (got ${JSON.stringify(mode)})`
  )
  process.exit(1)
}

const content = `// Copyright Offene Werkstatt Wädenswil
// SPDX-License-Identifier: MIT

// AUTO-GENERATED by scripts/inject-mode.mjs — do not edit by hand. Each
// \`npm run build:* / dev:*\` invocation rewrites this file before tsc runs.

import type { BridgeMode } from "./types"

export const BRIDGE_MODE: BridgeMode = "${mode}"
`

writeFileSync(target, content)
console.log(`inject-mode: wrote BRIDGE_MODE=${mode} to ${target}`)
