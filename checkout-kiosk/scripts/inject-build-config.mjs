// Writes src/build-config.generated.ts with the constants baked into
// the current Electron binary. Called from `npm run build:kiosk`,
// `dev:kiosk`, and `typecheck` — the operator sets the env vars.
//
// Why build-time (not runtime env vars)? OWW runs exactly one kiosk
// host. Shipping a self-contained binary is simpler than managing a
// systemd EnvironmentFile per machine, and rotation is just "replace
// the .AppImage on the trusted host" — no chance of forgetting to set
// an env var after a restart.
//
// `--prod` profile fills in production defaults from two sources:
//   * URL comes from the operations repo's `config.jsonc` (the same
//     file `scripts/generate-env.ts` reads, so checkout web +
//     Electron + functions stay in sync).
//   * Bearer comes from Google Secret Manager (`KIOSK_BEARER_KEY`,
//     same secret Cloud Functions reads server-side).
// Explicit env vars always win, so a staging URL + prod bearer combo
// is just a matter of setting BRIDGE_KIOSK_URL=... before invoking.

import { execSync } from "node:child_process"
import { existsSync, readFileSync, writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join, resolve } from "node:path"

const __dirname = dirname(fileURLToPath(import.meta.url))
const target = join(__dirname, "..", "src", "build-config.generated.ts")

const profile = process.argv.includes("--prod") ? "prod" : "dev"

// ── Production defaults sourced from ops config ─────────────────────
const opsDefaults = profile === "prod" ? loadProdDefaults() : null

// ── URL (defaults to localhost or ops config) ───────────────────────
const urlEnvVar = "BRIDGE_KIOSK_URL"
const devDefaultUrl = "https://localhost:5173/?kiosk"
const defaultUrl = opsDefaults?.url ?? devDefaultUrl
const url = process.env[urlEnvVar] || defaultUrl
const isDev = url.includes("localhost")

// ── Bearer ──────────────────────────────────────────────────────────
// In `--prod`: fetch from gcloud Secret Manager (`KIOSK_BEARER_KEY`)
// unless the operator already set BRIDGE_BEARER_KEY explicitly.
// Otherwise: take the env var verbatim, default to empty.
let bearer = process.env.BRIDGE_BEARER_KEY ?? ""
if (!bearer && profile === "prod") {
  bearer = fetchBearerFromGcloud()
}
if (!bearer && !isDev) {
  fail(
    `BRIDGE_BEARER_KEY must be set when ${urlEnvVar} points at a non-` +
      `localhost URL (got ${JSON.stringify(url)}). The runtime refuses ` +
      `to start without it; refusing the build is safer.`,
  )
}

const content = `// Copyright Offene Werkstatt Wädenswil
// SPDX-License-Identifier: MIT

// AUTO-GENERATED by scripts/inject-build-config.mjs — do not edit by hand.
// Each \`npm run build:* / dev:* / typecheck\` invocation rewrites this
// file before tsc runs; values come from env vars (and --prod defaults
// sourced from the operations repo) on the build host.

export const BRIDGE_URL: string = ${JSON.stringify(url)}
export const BRIDGE_BEARER_KEY: string = ${JSON.stringify(bearer)}
`

writeFileSync(target, content)
const redactedBearer = bearer ? `(set, ${bearer.length} chars)` : "(empty)"
console.log(
  `inject-build-config: profile=${profile} url=${url} ` +
    `bearer=${redactedBearer}`,
)

// ── Helpers ────────────────────────────────────────────────────────

function loadProdDefaults() {
  const projectRoot = resolve(__dirname, "..", "..")
  const configDir =
    process.env.OPERATIONS_CONFIG_DIR ||
    resolve(projectRoot, "..", "machine-auth-operations")
  const configPath = join(configDir, "config.jsonc")
  if (!existsSync(configPath)) {
    fail(
      `Operations config not found at ${configPath}. The --prod profile ` +
        `reads URLs from the operations repo. Clone it as ` +
        `a sibling of machine-auth/, or set OPERATIONS_CONFIG_DIR.`,
    )
  }
  const cfg = parseJsonc(readFileSync(configPath, "utf-8"))

  const domain = readPath(cfg, "web.checkoutDomain", configPath)
  return { url: `https://${domain}/?kiosk` }
}

function readPath(cfg, jsonPath, configPath) {
  const parts = jsonPath.split(".")
  let current = cfg
  for (const part of parts) {
    if (current == null || typeof current !== "object") {
      fail(
        `Missing required key "${jsonPath}" in ${configPath}. ` +
          `Add it to the operations config and re-run.`,
      )
    }
    current = current[part]
  }
  if (typeof current !== "string" || current.length === 0) {
    fail(
      `Expected non-empty string at "${jsonPath}" in ${configPath} ` +
        `(got ${JSON.stringify(current)}).`,
    )
  }
  return current
}

// jsonc parser — copy of the strip-comments logic in
// scripts/generate-env.ts (kept duplicated to avoid pulling that
// 500-line module into this build script).
function parseJsonc(text) {
  let result = ""
  let i = 0
  while (i < text.length) {
    if (text[i] === '"') {
      const start = i
      i++
      while (i < text.length && text[i] !== '"') {
        if (text[i] === "\\") i++
        i++
      }
      i++
      result += text.slice(start, i)
    } else if (text[i] === "/" && text[i + 1] === "/") {
      while (i < text.length && text[i] !== "\n") i++
    } else if (text[i] === "/" && text[i + 1] === "*") {
      i += 2
      while (i < text.length - 1 && !(text[i] === "*" && text[i + 1] === "/")) i++
      i += 2
    } else {
      result += text[i]
      i++
    }
  }
  return JSON.parse(result)
}

function fetchBearerFromGcloud() {
  console.log(
    "inject-build-config: fetching KIOSK_BEARER_KEY from gcloud Secret Manager…",
  )
  try {
    return execSync(
      "gcloud secrets versions access latest --secret=KIOSK_BEARER_KEY",
      { encoding: "utf-8" },
    ).trim()
  } catch (err) {
    fail(
      `Failed to fetch KIOSK_BEARER_KEY from gcloud. Check:\n` +
        `  • gcloud config get-value project  (must be the OWW project)\n` +
        `  • gcloud auth list                  (must have Secret Manager access)\n` +
        `Underlying error: ${err.message ?? err}`,
    )
  }
}

function fail(msg) {
  console.error(`inject-build-config: ${msg}`)
  process.exit(1)
}
