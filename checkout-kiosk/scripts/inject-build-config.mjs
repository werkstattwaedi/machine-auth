// Writes src/build-config.generated.ts with the constants baked into
// the current Electron binary. Called from `npm run build:kiosk`,
// `build:admin`, `dev:kiosk`, `dev:admin`, and `typecheck` — the npm
// scripts set BRIDGE_MODE in the env, the operator sets the rest.
//
// Why build-time (not runtime env vars)? OWW runs exactly one kiosk
// and one admin host. Shipping a self-contained binary is simpler than
// managing a separate systemd EnvironmentFile per machine, and
// rotation is just "replace the .AppImage on the trusted host" — no
// chance of forgetting to set an env var after a restart.

import { writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const __dirname = dirname(fileURLToPath(import.meta.url))
const target = join(__dirname, "..", "src", "build-config.generated.ts")

// ── Required: mode ──────────────────────────────────────────────────
const mode = process.env.BRIDGE_MODE
if (mode !== "kiosk" && mode !== "admin") {
  fail(
    `BRIDGE_MODE must be "kiosk" or "admin" (got ${JSON.stringify(mode)})`,
  )
}

// ── URL (per-mode env var, defaults to localhost) ───────────────────
const urlEnvVar = mode === "kiosk" ? "BRIDGE_KIOSK_URL" : "BRIDGE_ADMIN_URL"
const defaultUrl =
  mode === "kiosk"
    ? "https://localhost:5173/?kiosk"
    : "https://localhost:5174/"
const url = process.env[urlEnvVar] ?? defaultUrl
const isDev = url.includes("localhost")

// ── Bearer (required in production builds) ──────────────────────────
const bearer = process.env.BRIDGE_BEARER_KEY ?? ""
if (!bearer && !isDev) {
  fail(
    `BRIDGE_BEARER_KEY must be set when ${urlEnvVar} points at a non-` +
      `localhost URL (got ${JSON.stringify(url)}). The runtime refuses ` +
      `to start without it; refusing the build is safer.`,
  )
}

// ── Printer host (optional; admin-only in practice) ─────────────────
//   "" / unset → no printer → admin's "print" feature stays hidden.
//   "host"     → port 9100.
//   "host:N"   → explicit port.
const printerHost = process.env.BRIDGE_PRINTER_HOST ?? ""

const content = `// Copyright Offene Werkstatt Wädenswil
// SPDX-License-Identifier: MIT

// AUTO-GENERATED by scripts/inject-build-config.mjs — do not edit by hand.
// Each \`npm run build:* / dev:* / typecheck\` invocation rewrites this
// file before tsc runs; values come from env vars on the build host.

import type { BridgeMode } from "./types"

export const BRIDGE_MODE: BridgeMode = ${JSON.stringify(mode)}
export const BRIDGE_URL: string = ${JSON.stringify(url)}
export const BRIDGE_BEARER_KEY: string = ${JSON.stringify(bearer)}
export const BRIDGE_PRINTER_HOST: string = ${JSON.stringify(printerHost)}
`

writeFileSync(target, content)
const redactedBearer = bearer ? `(set, ${bearer.length} chars)` : "(empty)"
const printerLabel = printerHost ? printerHost : "(unset)"
console.log(
  `inject-build-config: mode=${mode} url=${url} ` +
    `bearer=${redactedBearer} printer=${printerLabel}`,
)

function fail(msg) {
  console.error(`inject-build-config: ${msg}`)
  process.exit(1)
}
