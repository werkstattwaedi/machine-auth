// Writes src/build-config.generated.ts with the constants baked into
// the current Electron binary. Called from `npm run build:kiosk`,
// `build:admin`, `dev:kiosk`, `dev:admin`, and `typecheck` — the npm
// scripts set BRIDGE_MODE in the env, the operator sets the rest.
//
// Why build-time (not runtime env vars)? OWW runs exactly one kiosk
// and one admin host. Shipping a self-contained binary is simpler than
// managing a separate systemd EnvironmentFile per machine, and
// rotation is just "replace the .AppImage on the trusted host" — no
// chance of forgetting to set an env var after a restart.
//
// `--prod` profile: fills in production defaults so a production build
// is a one-liner. URLs are hardcoded here (see `PROD_DEFAULTS`); the
// bearer is fetched from Google Secret Manager (`KIOSK_BEARER_KEY`,
// same secret Cloud Functions reads server-side); the printer host is
// the OWW LAN labeler. Any explicit env var still wins, so you can
// override single values without losing the rest.

import { execSync } from "node:child_process"
import { writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const __dirname = dirname(fileURLToPath(import.meta.url))
const target = join(__dirname, "..", "src", "build-config.generated.ts")

const profile = process.argv.includes("--prod") ? "prod" : "dev"

// Production defaults pulled in by `--prod`. Hardcoded here rather than
// read from the operations repo because (a) these URLs change roughly
// never, (b) cloning the ops repo solely to run a build adds friction,
// and (c) the values are public DNS so there's nothing to leak.
const PROD_DEFAULTS = {
  kiosk: {
    url: "https://checkout.werkstattwaedi.ch/?kiosk",
  },
  admin: {
    url: "https://admin.werkstattwaedi.ch/",
    printerHost: "labeler.internal:9100",
  },
}

// ── Required: mode ──────────────────────────────────────────────────
const mode = process.env.BRIDGE_MODE
if (mode !== "kiosk" && mode !== "admin") {
  fail(
    `BRIDGE_MODE must be "kiosk" or "admin" (got ${JSON.stringify(mode)})`,
  )
}

// ── URL (per-mode env var, defaults to localhost or PROD_DEFAULTS) ──
const urlEnvVar = mode === "kiosk" ? "BRIDGE_KIOSK_URL" : "BRIDGE_ADMIN_URL"
const devDefaultUrl =
  mode === "kiosk"
    ? "https://localhost:5173/?kiosk"
    : "https://localhost:5174/"
const defaultUrl = profile === "prod" ? PROD_DEFAULTS[mode].url : devDefaultUrl
const url = process.env[urlEnvVar] || defaultUrl
const isDev = url.includes("localhost")

// ── Bearer ──────────────────────────────────────────────────────────
// In `--prod`: fetch from gcloud Secret Manager (`KIOSK_BEARER_KEY`)
// unless the operator already set BRIDGE_BEARER_KEY explicitly.
// Otherwise: take the env var verbatim, default to empty.
let bearer = process.env.BRIDGE_BEARER_KEY ?? ""
if (!bearer && profile === "prod") {
  bearer = fetchBearerFromGcloud()
}
if (!bearer && !isDev) {
  fail(
    `BRIDGE_BEARER_KEY must be set when ${urlEnvVar} points at a non-` +
      `localhost URL (got ${JSON.stringify(url)}). The runtime refuses ` +
      `to start without it; refusing the build is safer.`,
  )
}

// ── Printer host (optional; admin-only in practice) ─────────────────
//   "" / unset → no printer → admin's "print" feature stays hidden.
//   "host"     → port 9100.
//   "host:N"   → explicit port.
const printerDefault =
  profile === "prod" && mode === "admin" ? PROD_DEFAULTS.admin.printerHost : ""
const printerHost = process.env.BRIDGE_PRINTER_HOST || printerDefault

const content = `// Copyright Offene Werkstatt Wädenswil
// SPDX-License-Identifier: MIT

// AUTO-GENERATED by scripts/inject-build-config.mjs — do not edit by hand.
// Each \`npm run build:* / dev:* / typecheck\` invocation rewrites this
// file before tsc runs; values come from env vars (and --prod defaults)
// on the build host.

import type { BridgeMode } from "./types"

export const BRIDGE_MODE: BridgeMode = ${JSON.stringify(mode)}
export const BRIDGE_URL: string = ${JSON.stringify(url)}
export const BRIDGE_BEARER_KEY: string = ${JSON.stringify(bearer)}
export const BRIDGE_PRINTER_HOST: string = ${JSON.stringify(printerHost)}
`

writeFileSync(target, content)
const redactedBearer = bearer ? `(set, ${bearer.length} chars)` : "(empty)"
const printerLabel = printerHost ? printerHost : "(unset)"
console.log(
  `inject-build-config: profile=${profile} mode=${mode} url=${url} ` +
    `bearer=${redactedBearer} printer=${printerLabel}`,
)

function fetchBearerFromGcloud() {
  console.log(
    "inject-build-config: fetching KIOSK_BEARER_KEY from gcloud Secret Manager…",
  )
  try {
    return execSync(
      "gcloud secrets versions access latest --secret=KIOSK_BEARER_KEY",
      { encoding: "utf-8" },
    ).trim()
  } catch (err) {
    fail(
      `Failed to fetch KIOSK_BEARER_KEY from gcloud. Check:\n` +
        `  • gcloud config get-value project  (must be the OWW project)\n` +
        `  • gcloud auth list                  (must have Secret Manager access)\n` +
        `Underlying error: ${err.message ?? err}`,
    )
  }
}

function fail(msg) {
  console.error(`inject-build-config: ${msg}`)
  process.exit(1)
}
